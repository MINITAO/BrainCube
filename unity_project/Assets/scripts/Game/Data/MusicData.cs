	public int	  id;
	public string title;
	public double speed;
	public int clap;
	public int[]	notes;
	public double[]	timeLine;
	
	public MusicData(int id, string title, double speed, int clap, int[] notes, double[] timeLine){
		this.id = id;
		this.title = title;
		this.speed = speed;
		this.clap = clap;
		this.notes = notes;
		this.timeLine = timeLine;
	}
	
	public int GetLength(){
		return (notes.Length > timeLine.Length) ? timeLine.Length : notes.Length;
	}

